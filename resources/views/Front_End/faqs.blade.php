<script>
    const tabIndicators = document.querySelectorAll('.faqs-indicators li')
    const customerTab = document.getElementById('customer-tab')
    const sellerTab = document.getElementById('seller-tab')
    tabIndicators.forEach((indicator) => {
        indicator.addEventListener('click', () => {
            const targetTab = indicator.dataset.tab;
            if (targetTab == 'customer') {
                sellerTab.setAttribute('aria-visible', 'false')
                customerTab.setAttribute('aria-visible', 'true')
                removeActiveClass()
                indicator.classList.add('active')
            } else {
                customerTab.setAttribute('aria-visible', 'false')
                sellerTab.setAttribute('aria-visible', 'true')
                removeActiveClass()
                indicator.classList.add('active')

            }
        })
    })
    const accordionButtons = Array.from(
        document.querySelectorAll(".accordion-item-title button")
    );
    const accordionItems = Array.from(
        document.querySelectorAll(".accordion-item ")
    );
    accordionButtons.forEach((btn) => {
        btn.addEventListener("click", () => {
            const parentAccordionItem = btn.parentElement.parentElement;
            if (parentAccordionItem.getAttribute("aria-visible") == "true") {
                parentAccordionItem.setAttribute("aria-visible", "false");
            } else {
                // accordionItems.forEach((accItem) => {
                //     accItem.setAttribute("aria-visible", "false");
                // });
                parentAccordionItem.setAttribute("aria-visible", "true");
            }
        });
    });


    function removeActiveClass() {
        tabIndicators.forEach((btn) => {
            btn.classList.remove('active')
        })
    }
</script>
